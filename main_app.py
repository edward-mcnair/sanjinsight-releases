        """Persist window geometry and tab splitter sizes."""
        import config as _cfg
        try:
            _cfg.set_pref("ui.geometry",
                          self.saveGeometry().toHex().data().decode())
        except Exception:
            log.debug("_save_layout: saveGeometry failed", exc_info=True)
